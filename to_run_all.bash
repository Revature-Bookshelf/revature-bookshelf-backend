# to run docker compose file
docker-compose 

# to start services in background
docker-compose up -d

# to show containers running 
docker ps 

# to show data container 
docker volume ls

# to connect to mongoserver 
mongo admin -u root -p rootpassword

# show dbs
show dbs

# show collections 
show collections

# To shut down without deleting containers 
docker-compose stop

# to shut down and delete containers 
docker-compose down 

